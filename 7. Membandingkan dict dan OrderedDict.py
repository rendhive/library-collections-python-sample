import collections

normal_dict = {'apple': 1, 'banana': 2, 'orange': 3}
ordered_dict = collections.OrderedDict([('apple', 1), ('banana', 2), ('orange', 3)])

print("Normal dict:", normal_dict)
print("Ordered dict:", ordered_dict)
# Fungsi: Menunjukkan perbedaan antara dict biasa dan OrderedDict.
