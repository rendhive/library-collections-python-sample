import collections

items = ['apple', 'orange', 'apple', 'banana', 'orange', 'apple']
counter = collections.Counter(items)

print("Frekuensi item dalam daftar:", counter)
# Fungsi: Menghitung frekuensi setiap elemen dalam list.
# Kondisi: Ketika Anda ingin mengetahui seberapa sering setiap elemen muncul di dalam list.
